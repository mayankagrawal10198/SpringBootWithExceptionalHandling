package com.example.userList.util;

public class ResponseBean {
    private int id;
    private String message;

    public ResponseBean() {

    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }
}
